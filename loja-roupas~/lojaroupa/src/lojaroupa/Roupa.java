package lojaroupa;

/**
 *
 * @author marcos.comper
 */
public class Roupa {
    private int id;
    private String cor;
    private String tamanho;
    private String marca;
    private String sexo;

    public Roupa(int id, String cor, String tamanho, String marca, String sexo) {
        this.id = id;
        this.cor = cor;
        this.tamanho = tamanho;
        this.marca = marca;
        this.sexo = sexo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getTamanho() {
        return tamanho;
    }

    public void setTamanho(String tamanho) {
        this.tamanho = tamanho;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }
    
}
